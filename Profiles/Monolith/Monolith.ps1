## Start profile timing
$ProfileStartTime = Get-Date

## Get the profile directory and ProfileComponents subdirectory
$ProfileDir = Split-Path -Path $PROFILE -Parent
$ComponentsDir = Join-Path $ProfileDir "ProfileComponents"

