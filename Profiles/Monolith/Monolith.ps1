####################

## Add custom modules directory to module path for auto-discovery
$CustomModulesPath = Join-Path $ProfileDir "Modules\Custom"
if (Test-Path $CustomModulesPath) {
    $env:PSModulePath = "$CustomModulesPath;$env:PSModulePath"
}

## Load prompt configuration (generated by Install-MonoProfile.ps1)
## If prompt-config.ps1 doesn't exist, $PromptHandler will be unset and
## the prompt.ps1 component will use the default prompt handler
$PromptConfigPath = Join-Path $ProfileDir "prompt-config.ps1"
if (Test-Path $PromptConfigPath) {
    . $PromptConfigPath
}

## Source all component files
$components = @(
    'namespaces.ps1'
    # 'psreadline-handlers.ps1'  # Deferred - loads in background via OnIdle
    'prompt.ps1'
    # 'shell-completions.ps1'    # Deferred - loads in background via OnIdle
    'aliases.ps1'
    'software-init.ps1'
)

foreach ( $component in $components ) {
    $componentPath = Join-Path $ComponentsDir $component
    if ( Test-Path $componentPath ) {
        try {
            # Measure component load time
            # $compStart = Get-Date

            . $componentPath
            
            ## Print components that take longer than threshold to load
            # $compEnd = Get-Date
            # $compTime = ($compEnd - $compStart).TotalMilliseconds
            # if ($compTime -gt 50) {
            #     Write-Host "  $component - ${compTime}ms" -ForegroundColor Yellow
            # }
        }
        catch {
            Write-Warning "Failed to load $component : $($_.Exception.Message)"
        }
    }
    else {
        Write-Warning "Component not found: $componentPath"
    }
}

## Defer PSReadLine handlers to background for faster startup
$psReadLineHandlersPath = Join-Path $ComponentsDir "psreadline-handlers.ps1"
if (Test-Path $psReadLineHandlersPath) {
    $null = Register-EngineEvent -SourceIdentifier PowerShell.OnIdle -MaxTriggerCount 1 -Action ([scriptblock]::Create(@"
        try {
            . '$($psReadLineHandlersPath -replace "'", "''")'
        }
        catch {
            Write-Warning "Failed to load deferred PSReadLine handlers: `$(`$_.Exception.Message)"
        }
"@))
}

## Defer shell completions to background (not needed until user presses Tab)
$shellCompletionsPath = Join-Path $ComponentsDir "shell-completions.ps1"
if (Test-Path $shellCompletionsPath) {
    $null = Register-EngineEvent -SourceIdentifier PowerShell.OnIdle -MaxTriggerCount 1 -Action ([scriptblock]::Create(@"
        try {
            . '$($shellCompletionsPath -replace "'", "''")'
        }
        catch {
            Write-Warning "Failed to load deferred shell completions: `$(`$_.Exception.Message)"
        }
"@))
}

## Defer posh-git to background for faster startup
#  Note: availability check moved inside OnIdle to avoid synchronous Get-Module -ListAvailable scan
$null = Register-EngineEvent -SourceIdentifier PowerShell.OnIdle -MaxTriggerCount 1 -Action {
    try {
        if (Get-Module -ListAvailable -Name posh-git) {
            Import-Module posh-git -ErrorAction Stop
            # Disable posh-git prompt (Starship handles the prompt)
            $GitPromptSettings.EnablePromptStatus = $false
        }
    }
    catch {
        Write-Warning "Failed to import posh-git: $($_.Exception.Message)"
    }
}

